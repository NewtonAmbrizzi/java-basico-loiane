public class Carro {
    
    private String marca;
    private String modelo;
    private int numeroPassageiros;
    private double capTanqueCombustivel;
    private double autonomia;

    public String getMarca() {
        return this.marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getNumeroPassageiros() {
        return numeroPassageiros;
    }

    public void setNumeroPassageiros(int numeroPassageiros) {
        this.numeroPassageiros = numeroPassageiros;
    }

    public double getCapTanqueCombustivel() {
        return capTanqueCombustivel;
    }

    public void setCapTanqueCombustivel(double capTanqueCombustivel) {
        this.capTanqueCombustivel = capTanqueCombustivel;
    }

    public double getAutonomia() {
        return autonomia;
    }

    public void setAutonomia(double autonomia) {
        this.autonomia = autonomia;
    }
    
    
    
}
